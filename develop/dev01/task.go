import (
	"fmt"
	"github.com/beevik/ntp"
	"os"
)

func main() {
	// получаем время NTP сервера точного времени
	time, err := ntp.Time("0.beevik-ntp.pool.ntp.org")
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}

	// печатаем отформатированный результат
	fmt.Printf("Current time: %v\n", time.Format("15:04:05"))
	fmt.Printf("Exact time: %v", time.Format("15:04:05.99999999"))
}
